<p>
From time to time, technology websites come and go, some with
interesting content, some not that much. But there's one thing
in common among many of them: content is often optimized for
machines instead of people.
</p>

<p>
We at thePHP Website believe all developers, no matter which
seniority level, deserve being treated like humans. Without
hidden contexts, arrogance and no inflated egos.
</p>

<p>
The first author of this website, <a href="https://twitter.com/nawarian" rel="nofollow">@nawarian</a>,
is a Brazilian born in Mogi das Cruzes, SÃ£o Paulo. Since the beginning
of his career he was part of the <a href="https://phpsp.org.br/" rel="nofollow">PHPSP</a>
community, where he learned about how communities work.
</p>

<p>
<strong>This website is for the community</strong>,
<a href="https://github.com/nawarian/The-PHP-Website" rel="nofollow">is open</a>
and looks simple. The biggest intention is to offer the richest
content as possible with maximum respect for its users.
</p>

<h2>thePHP Website's Values</h2>

<p>
Since its beginning and for ever, thePHP Website will bring with itself,
by relevance order the following values:
</p>

<ol>
  <li>Be respectful</li>
  <li>Be direct</li>
  <li>Be open</li>
</ol>

<h3>1. Be respectful</h3>
<p>
The main value thePHP Website carries is the <strong>respect for its
users</strong>. This is translated from the content to the design and
layout choices.
</p>

<p>
The respect for reading, for user resources like network, battery,
visual effort.
</p>

<p>
Like good engineers we all want a content that brings value much
more than a cool interface and full of useless clutter that only
consume and disrespect our time and resources.
</p>

<p>
We also don't want BS, half truths or data with no sources. We want
opinions based on facts, texts with sources and proofs. <strong>
We want code examples that look like daily engineering life ones</strong>,
not things like <code>$dog->eat($rice)</code> and things that will only
make sense on a child book.
</p>

<h3>2. Be direct</h3>
<p>
Every post aiming to answer a question or topic will do so. No
fairy tales or unrelated texts with no connection to the subject.
</p>

<p>
<strong>This cheap SEO</strong> made by repeating keywords, creating
fake links and repeating words/citations that are not relevant at
all to the content <strong>won't be accepted here.</strong>
</p>

<p>
Every reader can feel free to complain about a content containing
unnecessary information or long introductions. We don't care if
our readers spend only 20 seconds in our pages, as far as these
20 seconds are enough to bring the information being seeked.
</p>

<h3>3. Be open</h3>
<p>
thePHP Website was born open and open it will remain. We'll accept
content from whoever wishes to share with us, as soon as it aligns
to our values above.
</p>

<p>
Every decision, numbers, statistics and plans can be asked by any
person anytime.
</p>

<p>
<strong>But don't be fooled!</strong> thePHP Website is proprietary and
commercial. Even though its intention is to return profits to the
community in the form of services and content, there's still a team
made of people receiving money for it.
</p>

<p>
So expect ads, banners or sponsored links from time to time. But you
can also be sure they will ALWAYS be relevant to the content.
</p>

<h2>thePHP Website has a mission</h2>

<p>
thePHP Website's mission is to warm again PHP community and bring
professional content to its readers.
</p>

<p>
We understand that the programmer's profession is one of the
best ways to support individual social mobility and has great
potential in developing countries like Brazil. And that content,
services and courses development that support this are essential
and should be considered on every word we choose writing.
</p>

<p>
thePHP Website is still young, but brings in its heart a big and
explicit will to deliver service and content of great quality.
</p>

<p>
Your content here will always be received and accepted. Code changes
as well. Everything that enables us to grow together, as a community.
</p>

